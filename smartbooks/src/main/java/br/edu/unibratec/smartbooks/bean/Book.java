package br.edu.unibratec.smartbooks.bean;

public class Book {

	
	private int idbook;
	private String titulo;
	

	public int getIdbook() {
		return idbook;
	}
	public void setIdbook(int idbook) {
		this.idbook = idbook;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
}
